import mesk

class PluginInfo(dict):
    def __init__(self, name=None, desc=None, author=None, url=None,
                 copyright=None, clazz=None, xpm=None, display_name=None):
        '''Despite the default arguments all except xpm are required'''
        self['NAME'] = name
        self['DESCRIPTION'] = desc
        self['AUTHOR'] = author
        self['URL'] = url
        self['COPYRIGHT'] = copyright
        self['XPM'] = xpm or DEFAULT_PLUGIN_XPM
        self['CLASS'] = clazz
        self['DISPLAY_NAME'] = display_name

        # Ensure all required values were set
        for key in self:
            if not self[key]:
                raise KeyError('Missing value in PluginInfo for key \'%s\'' %
                               key)

class Plugin(object):
    name = property(lambda self: self.__plugin_info['NAME'])
    description = property(lambda self: self.__plugin_info['DESCRIPTION'])
    author = property(lambda self: self.__plugin_info['AUTHOR'])
    url = property(lambda self: self.__plugin_info['URL'])
    copyright = property(lambda self: self.__plugin_info['COPYRIGHT'])
    xpm = property(lambda self: self.__plugin_info['XPM'])

    def __init__(self, plugin_info):
        self.__plugin_info = plugin_info

        # A logger for each specific plugin
        self.log = mesk.log.getLogger(self.name)
        # The configuration window
        self.config_glade = None
        self.config_window = None

    def shutdown(self):
        '''Called when the plugin is shutdown or deactivated'''

    def is_configurable(self):
        '''Return True if the plugin supports configuration.
           Defaults to False'''
        return False

    def get_config_widget(self, parent):
        '''Return the configuration widget.'''
        return None
    def config_ok(self):
        '''Called when the configuration Ok button is clicked'''
    def config_cancel(self):
        '''Called when the configuration Cancel button is clicked'''

